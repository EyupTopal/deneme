package Alistirmalar;
// while döngüsü ile ekrana 40 defa Kapsamli java egitimi yazdirin?
public class Soru18_WhileDongusu {
    public static void main(String[] args) {
        int sayi=0;
        while (sayi<40){
            System.out.println ((sayi+1)+ " Kapsamli java egitimi");
            sayi=sayi+1;
        }
    }
}
